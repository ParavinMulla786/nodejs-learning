function addition(x, y) {
    return x + y;
}

function subtraction(x, y) {
    return x - y;
}

function multiplication(x, y) {
    return x * y;
}

function division(x, y) {
    return x / y;
}

function square(x) {
    return x * x;
}

// export all functions
module.exports = {
    addition,
    subtraction,
    multiplication,
    division,
    square
};